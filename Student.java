package com.javacodes.example.model;

public class Student {
	private int id;
	private String name;
	private int grade;
	
	public Student() {}
	
	public Student(int id, String name,int grades) {
	this.id = id;
	this.name = name;
	this.grade = grade;
	}
	
	public int getId() {
		return id;
	}
	public void setId() {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getGrades() {
			return getGrades();
	}
	public void setGrades(int grades) {
			this.grade = grades;
	}
}
